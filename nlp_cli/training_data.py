import json
from pathlib import Path
import os

def get_custom_data_path() -> Path:
    """Get path to the custom training data file."""
    # Use ~/.nlp_cli/custom_data.json
    home = Path.home()
    data_dir = home / ".nlp_cli"
    data_dir.mkdir(exist_ok=True)
    return data_dir / "custom_data.json"


def load_custom_data() -> List[Tuple[str, str]]:
    """Load custom training data from JSON file."""
    path = get_custom_data_path()
    if not path.exists():
        return []
    
    try:
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
            # Ensure it's a list of [text, intent]
            return [(item[0], item[1]) for item in data if len(item) >= 2]
    except Exception:
        return []


def save_custom_data(data: List[Tuple[str, str]]):
    """Save custom training data to JSON file."""
    path = get_custom_data_path()
    try:
        with open(path, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2)
    except Exception as e:
        print(f"Error saving custom data: {e}")


def add_training_example(text: str, intent: str) -> bool:
    """
    Add a new training example and save it.
    
    Args:
        text: Natural language text.
        intent: Command intent.
        
    Returns:
        True if successful.
    """
    custom_data = load_custom_data()
    
    # Check if already exists
    for t, i in custom_data:
        if t.lower() == text.lower() and i == intent:
            return False
            
    custom_data.append((text, intent))
    save_custom_data(custom_data)
    return True


def get_training_data() -> List[Tuple[str, str]]:
    """Return the training data (Default + Custom)."""
    return DEFAULT_TRAINING_DATA + load_custom_data()


def get_intent_list() -> List[str]:
    """Return list of unique intents."""
    data = get_training_data()
    return list(set(intent for _, intent in data))


def get_examples_for_intent(intent: str) -> List[str]:
    """Get all example phrases for a specific intent."""
    data = get_training_data()
    return [text for text, i in data if i == intent]

